
public class binary {
    public static void main(String args[]){
        System.out.println(5 & 6); // & Operation
        System.out.println(5 | 6); // | Operation
        System.out.println(5 ^ 6); // ^ Operation
        System.out.println(~5); // ~ Operation
        System.out.println(~0); // ~ Complement Operation
        System.out.println(5 << 2); // << Lest Shift Operation
        System.out.println(6 >> 1); // << Right Shift Operation

    }
    
}
