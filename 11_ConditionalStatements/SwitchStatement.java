
public class SwitchStatement {
    public static void main(String[] args) {
        int num = 2; // char ch = 'a';
        switch (num) { //(ch)
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Bruger");
                break;
            case 3:
                System.out.println("Mango Shake");
                break;
            default:
                System.out.println("We wake up");
        }
    }
}
